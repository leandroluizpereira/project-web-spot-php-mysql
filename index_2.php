            <h1><strong style="color:#D9AD5B">NBA</strong><strong style="color: rgb(139, 22, 22);"> N</strong>ational <strong  style="color: rgb(139, 22, 22);">B</strong>asketball <strong  style="color: rgb(139, 22, 22);">A</strong>ssociation </h1>
            <p>A National Basketball Association (em português: Associação Nacional de Basquetebol; abreviação oficial: NBA) é a principal liga de basquetebol profissional da América do Norte. Com 30 franquias (29 nos Estados Unidos e 1 no Canadá), a NBA também é considerada a principal liga de basquete do mundo. É um membro ativo da USA Basketball (USAB)[1], que é reconhecida pela FIBA (a Federação Internacional de Basquetebol) como a entidade máxima e organizadora do basquetebol nos Estados Unidos. A NBA é uma das 4 'major leagues' de esporte profissional na América do Norte. Os jogadores da NBA são os mais bem pagos esportistas do mundo, por salário médio anual.[2]</p>
            <p>A liga foi fundada na cidade de Nova Iorque em 6 de Junho de 1946, como a Basketball Association of America (BAA).[3] A liga adotou o nome de National Basketball Association em 1949 quando se fundiu com a rival National Basketball League (NBL). A liga tem diversos escritórios ao redor do mundo, além de vários dos próprios clubes fora da sede principal na Olympic Tower localizada na Quinta Avenida 645. Os estúdios da NBA Entertainment e da NBA TV são localizados em Secaucus, New Jersey.</p>
            <p>Atualmente a liga é transmitida em Portugal pela Sport TV e pela NBA TV, e no Brasil pela ESPN e pelo SporTV. Nos Estados Unidos, as emissoras são NBA TV, TNT e ESPN, além da transmissão dos principais jogos pela ABC. O pacote NBA League Pass, que permite ao assinante assistir todos os jogos da liga ao vivo e em alta definição, é disponibilizado para assinatura em todo o mundo.</p>
            <h2>Criação e fusão</h2>
            <p>A BAA (Basketball Association of America) foi fundada em 1946 pelos proprietários das principais arenas de Hóquei no gelo no Nordeste e no Centro-Oeste dos Estados Unidos e Canadá. Em 1 de novembro de 1946, em Ontário, Canadá, os Toronto Huskies recebeu o New York Knickerbockers, no Maple Leaf Gardens, em um jogo que a NBA agora considera como o primeiro jogo em sua história.[4] Apesar de ter havido tentativas anteriores de criar ligas de basquete profissional, incluindo a American Basketball League e a NBL, a BAA foi a primeira liga à tentar jogar em grandes arenas nas grandes cidades. Durante seus primeiros anos, a qualidade do jogo na BAA não foi significativamente melhor do que nas ligas concorrentes ou contra os principais clubes independentes, como o Harlem Globetrotters. Por exemplo, o finalista da ABL de 1948, Baltimore Bullets, mudou-se para a BAA e conquistou o título da liga em 1948, e o Minneapolis Lakers, campeão da NBL em 1948, ganhou o título da BAA em 1949.</p>
            <p>m 3 de agosto de 1949, a BAA aceitou se fundir com a NBL, criando a nova National Basketball Association (NBA).[6] A nova liga tinha 17 franquias localizadas em uma mistura de cidades grandes e pequenas,[6] bem como grandes e pequenos ginásios. Em 1950, a NBA se consolidou em 11 franquias, um processo que continuou até 1953-54, quando o campeonato atingiu o seu menor tamanho de oito franquias, os quais todos ainda estão na liga (New York Knicks, Boston Celtics, Golden State Warriors, Los Angeles Lakers, Sacramento Kings, Detroit Pistons, Atlanta Hawks, e Philadelphia 76ers). O processo de contração fez com que as franquias das menores cidades se movessem para grandes mercados. Os Hawks saíram de Tri-Cities (conhecidas como Quad Cities) para Milwaukee em 1951 e então para St. Louis, Missouri em 1955; os Royals foram de Rochester para Cincinnati (em 1957); e os Pistons de Fort Wayne, Indiana para Detroit (em 1957).</p>
            <p>Enquanto isso, o Nipo-americano Wataru Misaka quebrou a barreira de raças na NBA na temporada 1948-49 quando jogou pelos New York Knicks, 1950 é conhecido como o ano em que a NBA se integrou. Em 26 de Abril de 1950, Harold Hunter assinou com os Washington Capitols, se tornando o primeiro Afro-americano a assinar um contrato com qualquer time na NBA na história.[7][8] Hunter foi cortado durante o training camp,[7][9] porém vários Afro-americanos começaram a jogar na liga no final daquele ano, como Chuck Cooper com os Celtics, Nathaniel Clifton com os Knicks e Earl Lloyd com os Washington Capitols. Nesse meio tempo, os Minneapolis Lakers, liderados pelo pivô George Mikan, ganhou 5 títulos da NBA e estabilizou o time pela primeira dinastia da NBA.[10] Para encorajar os arremessos e desencorajar "calmaria", a NBA criou o cronometro de 24 segundos em 1954.[11] Se um time não tentar um arremesso (ou não encostar no aro), em 24 segundos de jogada, a jogada acaba e a posse de bola passa para seu oponente.</p>
            <h2>Ascensão na popularidade</h2>
            <p>A liga adiciona o inovador arremesso de três pontos do ABA para abrir mais o jogo em 1979. O mesmo ano, os calouros Larry Bird e Magic Johnson se juntaram aos Celtics e aos Lakers, respectivamente, iniciando um período de aumento significativo no interesse dos fãs através dos Estados Unidos e do mundo. Em 1984, eles jogaram um contra o outro pela primeira vez nas finais da NBA. Johnson, junto de Kareem Abdul-Jabbar e James Worthy, liderou os Lakers a cinco títulos da NBA e Bird, junto de Kevin McHale e Robert Parish, levou a equipe a três títulos. Também no começo dos anos 80, a NBA adicionou um novo time de expansão, o Dallas Mavericks, levando a quantidade de equipes a 23. Outras equipes de destaque foram o 76ers de Julius "Dr. J" Erving, campeão em 1983 e o Detroit Pistons que com um time muito físico apelidado "Bad Boys", liderados por Isiah Thomas, Chuck Daly e Dennis Rodman, chegou a três finais seguidas e dois títulos em 1989 e 1990. David Stern assumiu o cargo de comissário da liga em 1 de Abril de 1984 e comandou a expansão e crescimento da NBA para um nível global.</p>
            <p>Hakeem Olajuwon entrou na liga em 1984 com o Houston Rockets, fornecendo uma estrela ainda mais popular para ajudar o aumento de interesse na liga. Isso resultou em mais cidades interessadas em ter uma equipe pertencente a ela. Em 1988 e 1989, quatro cidades tiveram seu desejo realizado com Charlotte Hornets, Miami Heat, Orlando Magic e Minnesota Timberwolves fazendo suas estreias na NBA, fazendo um total de 27 equipes. No primeiro ano da década de 1990, o Detroit Pistons viria a vencer seu segundo título, logo após de vencer seu primeiro em 1989, Michael Jordan e Scottie Pippen, junto com o técnico Phil Jackson, levariam os Bulls a seis títulos entre 1991 e 1998, sendo duas sequências de três títulos. Olajuwon venceu dois títulos consecutivos com os Rockets em 1994 e 1995.</p>
            <p>A equipe de basquetebol das Olimpíadas de 1992, o Dream Team, foi o primeiro a usar as estrelas da NBA, com Michael Jordan na liderança, junto com Magic, Bird, David Robinson, Patrick Ewing, Scottie Pippen, Clyde Drexler, Karl Malone, John Stockton, Chris Mullin, Charles Barkley e Christian Laettner. Onze jogadores do Dream Team foram introduzidos ao Basketball Hall of Fame.</p>
            <p>Em 1995, a NBA expandiu para o Canadá com a adição do Vancouver Grizzlies e do Toronto Raptors. Em 2001, o Vancouver Grizzlies se realocou para Memphis, o que deixou os Raptors como a única franquia canadense na liga. Em 1996, a NBA criou a liga feminina, a Women's National Basketball Association (WNBA). Em 1998, os donos de equipes da NBA começaram um lockout (greve), que durou 191 dias e teve fim em 18 de Janeiro de 1999. Como resultado, a temporada da NBA de 1998-99 foi reduzida de 82 para 50 jogos (61% de uma temporada normal) e o All-Star Game foi cancelado. O San Antonio Spurs venceu seu primeiro campeonato, e o primeiro de uma equipe antiga da ABA, derrotando o New York Knicks, que foi o primeiro e até hoje o único oitavo colocado de uma conferência a chegar às Finais da NBA.</p>
            <h2>Atualmente: Internacionalização</h2>
            <p>A liga começou a década de 2000 a investir na importação de grandes jogadores de diversas nações, como o espanhol Pau Gasol, o chinês Yao Ming, o alemão Dirk Nowitzki, o brasileiro Leandrinho Barbosa e o argentino Manu Ginóbili. A maioria dos jogadores de países de língua inglesa jogam em universidades americanas antes de ir para NBA, como o canadense MVP de 2005 e 2006 Steve Nash, e o australiano Andrew Bogut. Assim a NBA consegue um público internacional cada vez maior, sendo transmitida para 215 países por 90 canais em 47 idiomas.[17]</p>
            <p>A NBA chegou ao número atual de 30 equipes com a fundação do Charlotte Bobcats em 2004 para compensar a saída do Hornets para Nova Orleans em 2002. Outras relocações incluíram a saída do Grizzlies do Canadá para Memphis em 2001, a mudança do SuperSonics para Oklahoma City (que havia abrigado o Hornets enquanto Nova Orleans se recuperava do Furacão Katrina) para se tornar o Oklahoma City Thunder em 2008, e a volta do New Jersey Nets para Nova York como Brooklyn Nets em 2012.</p>
            <p>Phil Jackson foi contratado pelo Lakers e retornou o time de Los Angeles à proeminência na liga, com Kobe Bryant e Shaquille O'Neal vencendo três campeonatos seguido entre 2000 e 2002. Em seguida o Spurs de Duncan e o técnico Gregg Popovich, mais Manu Ginóbili e o francês Tony Parker (primeiro jogador a ser escolhido melhor das finais) ganhou três campeonatos em seis anos. Contratações para a formação de supertimes levaram à títulos do Miami Heat em 2005 - reforçando Dwyane Wade com O'Neal, Gary Payton e Alonzo Mourning - e do Celtics em 2008 - acrescentando Ray Allen e Kevin Garnett à estrela do time, Paul Pierce. Em seguida Jackson se tornaria o técnico mais vitorioso da história da NBA com um bicampeonato do Lakers de Bryant, Pau Gasol e Derek Fisher.</p>
            <p>Após o título do Lakers, começou uma era dominada por LeBron James, que já tinha levado o Cleveland Cavaliers para a final de 2007 (perdida para o Spurs). Desejando ganhar títulos, assinou com o Miami Heat, que renovou com Dwyane Wade e atraiu Chris Bosh em um investimento pesado para garantir novos campeonatos. De cara alcançaram a final de 2011, perdida para o Dallas Mavericks de Dirk Nowitzki, Jason Terry e Jason Kidd. Porém em seguida o Heat venceu dois títulos consecutivos, em 2012 sobre o Thunder de Kevin Durant e Russell Westbrook (em uma temporada encurtada por mais uma greve no final de 2011)[18] e em 2013 sobre os San Antonio Spurs, após forçar uma virada no jogo 6. Em 2014, a final foi novamente Heat contra os Spurs, desta vez com vitória de San Antonio, garantindo seu quinto título. Após a derrota, James decidiu voltar ao Cavaliers, considerando que a única conquista que lhe faltava era um título para o time de sua cidade natal. Acompanhado de Kyrie Irving e Kevin Love, James se tornou o primeiro jogador desde Bill Russell na década de 60 a alcançar 5 finais consecutivas.[19] Seu adversário na final era o Golden State Warriors, que com forte elenco liderado por Stephen Curry e Klay Thompson e muitas baixas por lesão no Cavs, conseguiu o tetracampeonato 40 anos depois do último título.</p>
