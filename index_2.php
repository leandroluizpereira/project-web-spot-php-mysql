        <p>Após o domínio nos dois primeiros anos das Alfettas e das antigas voiturettes, a Ferrari apresenta ucarro vencedor com motor de 4,5 litros e domina completamente os anos de 1952 e 1953, dando a Alberto Ascaro título de bicampeão. Neste momento, a Alfa, que competia ainda com as Alfettas (projeto do pré-guerra) nãtinha recursos financeiros para investir no desenvolvimento de um novo projeto e decide abandonar a categori</p>
        <p>Em 1954, a Mercedes-Benz retorna ao esporte com um carro perfeito que deu a Juan Manuel Fangio mais títulos, tornando-se tricampeão mundial. Os carros são menores, com motores de 2,5 litros. Ao final de 1955a Mercedes abandona as competições em razão da tragédia de Le Mans ocorrida naquele ano, quando mais de 8espectadores morreram quando a Mercedes de Pierre Levegh projetou-se sobre a multidão. Neste momento, Ferrari contrata Fangio, que conquista o quarto título na F-1. Em 1957, ele conquista seu quinto (e últimotítulo pela Maserati.</p>
        <p>Em 1955, a Vanwall, primeira equipe inglesa de F-1, apresenta um carro originalmente concebido para Fórmula 2 de 2,0 litros, porém equipado com freios a disco e injeção de combustível. Em 1956, a Vanwalapresenta o motor de 2,5 litros e um novo chassi concebido por Colin Chapman, que nesta época desenvolvicarros esporte para a Lotus. Após algumas modificações introduzidas na suspensão por Chapman e a contrataçãde um especialista em carrocerias, Frank Costin, o carro da Vanwall tornou-se extremamente competitivo. Parbrigar pelo campeonato foram contratados 2 excelentes pilotos: Stirling Moss e Tony Brooks. Assim, a Vanwalse tornou a primeira equipe campeã de construtores em 1958.</p>
        <p>Em 1958, a Cooper apresenta um pequeno carro (baseado nos modelos da Fórmula 3 de 500 cc) com motor dfabricação própria, montado na parte traseira, com um acentuado índice de avanço técnico comparado aos carroda época. Este carro marcou os modelos da década que se iniciava já sendo campeão de construtores e dpilotos, com o australiano Jack Brabham, em 1959 e 1960 (Brabham futuramente criaria sua própria equipe de Fe venceria um campeonato mundial com ela. Este feito inspirou também o neozelandês Bruce McLaren, que corrina F1 e que acabou criando a bem-sucedida equipe McLaren, onde correram grandes campeões, como Niki LaudaAlain Prost e Ayrton Senna).</p>
